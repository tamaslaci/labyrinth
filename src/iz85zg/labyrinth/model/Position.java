package iz85zg.labyrinth.model;

/**
 *
 * @author iz85zg
 */
public class Position {
    private final int x;
    private final int y;
    
    public Position(){
        this.x = 0;
        this.y = 0;
    }
    
    public Position(int x, int y){
        this.x = x;
        this.y = y;
    }
    
    public int getX(){
        return this.x;
    }
    
    public int getY(){
        return this.y;
    }
    
    public boolean adjacentTo(Position p){
        if(Math.abs(this.x - p.x) <= 1 && Math.abs(this.y - p.y) <= 1) return true;
        return false;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 97 * hash + this.x;
        hash = 97 * hash + this.y;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Position other = (Position) obj;
        if (this.x != other.x) {
            return false;
        }
        return this.y == other.y;
    }
}
